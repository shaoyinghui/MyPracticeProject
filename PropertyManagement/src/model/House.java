package model;

public class House {
	private int house_id;
	private String house_building;
	private int house_floor;
	private int house_unit;
	private int house_num;
	private float house_area;
	private int house_type;
	private String house_remark;
	public int getHouse_id() {
		return house_id;
	}
	public void setHouse_id(int house_id) {
		this.house_id = house_id;
	}
	public String getHouse_building() {
		return house_building;
	}
	public void setHouse_building(String house_building) {
		this.house_building = house_building;
	}
	public int getHouse_floor() {
		return house_floor;
	}
	public void setHouse_floor(int house_floor) {
		this.house_floor = house_floor;
	}
	public int getHouse_unit() {
		return house_unit;
	}
	public void setHouse_unit(int house_unit) {
		this.house_unit = house_unit;
	}
	public int getHouse_num() {
		return house_num;
	}
	public void setHouse_num(int house_num) {
		this.house_num = house_num;
	}
	public float getHouse_area() {
		return house_area;
	}
	public void setHouse_area(float house_area) {
		this.house_area = house_area;
	}
	public int getHouse_type() {
		return house_type;
	}
	public void setHouse_type(int house_type) {
		this.house_type = house_type;
	}
	public String getHouse_remark() {
		return house_remark;
	}
	public void setHouse_remark(String house_remark) {
		this.house_remark = house_remark;
	}

}
