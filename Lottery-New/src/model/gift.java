package model;

public class gift {
private int gift_id;
private String gift_name;
private int total_gift;
private int remain_gift;
public int getGift_id() {
	return gift_id;
}
public void setGift_id(int gift_id) {
	this.gift_id = gift_id;
}
public String getGift_name() {
	return gift_name;
}
public void setGift_name(String gift_name) {
	this.gift_name = gift_name;
}
public int getTotal_gift() {
	return total_gift;
}
public void setTotal_gift(int total_gift) {
	this.total_gift = total_gift;
}
public int getRemain_gift() {
	return remain_gift;
}
public void setRemain_gift(int remain_gift) {
	this.remain_gift = remain_gift;
}

}
