package model;

public class person {
private int person_id;
private String wechat_id;
private String nickname;
private boolean is_custom;
private int remaining_number;
private String person_name;
private String identity_id;
public int getPerson_id() {
	return person_id;
}
public void setPerson_id(int person_id) {
	this.person_id = person_id;
}
public String getWechat_id() {
	return wechat_id;
}
public void setWechat_id(String wechat_id) {
	this.wechat_id = wechat_id;
}
public String getNickname() {
	return nickname;
}
public void setNickname(String nickname) {
	this.nickname = nickname;
}
public boolean isIs_custom() {
	return is_custom;
}
public void setIs_custom(boolean is_custom) {
	this.is_custom = is_custom;
}
public int getRemaining_number() {
	return remaining_number;
}
public void setRemaining_number(int remaining_number) {
	this.remaining_number = remaining_number;
}
public String getPerson_name() {
	return person_name;
}
public void setPerson_name(String person_name) {
	this.person_name = person_name;
}
public String getIdentity_id() {
	return identity_id;
}
public void setIdentity_id(String identity_id) {
	this.identity_id = identity_id;
}


}
